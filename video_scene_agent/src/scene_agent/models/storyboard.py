
from __future__ import annotations

from typing import Literal, Optional

from pydantic import AliasChoices, BaseModel, ConfigDict, Field, model_validator

class WorldObject(BaseModel):
    """Canonical world object used by the new workflow."""

    id: str = Field(description="Stable short snake_case object identifier")
    name: str = Field(description="Display name")
    appearance: str = Field(description="Canonical appearance description")
    constraints: list[str] = Field(default_factory=list, description="Must-preserve constraints")
    story_role: Literal["character", "environment", "prop", "threat", "creature", "vehicle", "object"] = Field(
        default="object",
        description="Narrative role of the object inside the scene",
    )
    default_visibility: Literal["always_visible", "latent", "reveal_only", "conditional"] = Field(
        default="always_visible",
        description="Default visibility policy across the scene timeline",
    )
    reveal_rules: list[str] = Field(default_factory=list, description="Conditions that allow a full reveal")
    pre_reveal_hints: list[str] = Field(default_factory=list, description="Allowed pre-reveal hint phrases")
    hard_exclusions_before_reveal: list[str] = Field(
        default_factory=list,
        description="Explicit exclusions before the object is allowed to be revealed",
    )
class StoryStyleGuide(BaseModel):
    """Canonical style guide used by the new workflow."""

    style: str = Field(description="Visual style")
    palette: str = Field(description="Palette description")
    cinematic_intent: str = Field(default="", description="High-level cinematic intent for the scene")
    camera_language: str = Field(default="", description="Global camera-language guidance")
    lighting_logic: str = Field(default="", description="Global lighting logic shared across shots")
    continuity_bible: list[str] = Field(default_factory=list, description="Short reusable continuity lock phrases")
    global_continuity_locks: list[str] = Field(default_factory=list, description="Scene-wide continuity locks")
    public_continuity_locks: list[str] = Field(default_factory=list, description="Pre-reveal-safe continuity locks")
    reveal_locked_traits: list[str] = Field(default_factory=list, description="Locked traits used only after reveal")
    global_negative: list[str] = Field(default_factory=list, description="Scene-wide negative prompts")
class WorldDescription(BaseModel):
    """Canonical world package generated by the director."""

    scene_background: str = Field(description="Scene background/environment")
    objects: list[WorldObject] = Field(default_factory=list, description="Objects or characters in scene")
    primary_subject_ids: list[str] = Field(default_factory=list, description="Main hero or subject ids that must stay tracked across anchor frames")
    style_guide: StoryStyleGuide = Field(description="Global scene style guide")
class StoryBeatData(BaseModel):
    """Narrative beat used to decouple story progression from a fixed frame count."""

    model_config = ConfigDict(populate_by_name=True, extra="ignore")

    idx: int = Field(
        ge=0,
        validation_alias=AliasChoices("idx", "index"),
        serialization_alias="idx",
        description="0-based beat index",
    )
    id: str = Field(description="Stable beat identifier")
    label: str = Field(default="", description="Short human-readable beat label")
    narrative_function: str = Field(default="", description="Narrative function such as reveal or reaction")
    goal: str = Field(default="", description="What the beat must achieve on screen")
    emotion: str = Field(default="", description="Primary emotional state of the beat")
    visible_objects: list[str] = Field(default_factory=list, description="Object ids allowed to be fully visible")
    latent_objects: list[str] = Field(default_factory=list, description="Object ids that must stay hidden")
    allowed_hints: list[str] = Field(default_factory=list, description="Allowed indirect hints for latent objects")
    motion_intensity: str = Field(default="", description="Low/medium/high motion intensity guidance")
